"""
config.py — שכבת ההגדרות.

כאן נמצאים *רק נתונים*: אילו פעולות מותרות, רמת הסיכון שלהן, ושם המודל.
אין כאן שום לוגיקה ושום קריאות API. אם רוצים להוסיף/לשנות פעולה — נוגעים פה בלבד.
"""

# שם המודל בו נשתמש (חינמי ב-Gemini free tier)
MODEL = "gemini-2.5-flash"

# רמות הסיכון האפשריות — מקור אמת יחיד, כדי שלא יהיו "ערכים קסומים" בקוד
RISK_LEVELS = ["low", "medium", "high"]

# ---------------------------------------------------------------------------
# Hard-coded context: העולם שהמודל מכיר.
# המודל לא אמור להכיר שום פעולה שאינה ברשימה הזו.
# ---------------------------------------------------------------------------
ALLOWED_ACTIONS = {
    "delete_logs":     {"risk": "medium", "desc": "מחיקת קבצי לוג ישנים"},
    "send_email":      {"risk": "low",    "desc": "שליחת מייל"},
    "backup_files":    {"risk": "low",    "desc": "גיבוי קבצים"},
    "restart_service": {"risk": "high",   "desc": "הפעלה מחדש של שירות"},
    "list_files":      {"risk": "low",    "desc": "הצגת רשימת קבצים"},
    "disk_cleanup":    {"risk": "medium", "desc": "ניקוי שטח דיסק"},
}


def actions_as_text() -> str:
    """מחזיר את רשימת הפעולות כטקסט קריא, להזרקה לתוך הפרומפט."""
    return "\n".join(
        f"  - {name} (סיכון: {info['risk']}) — {info['desc']}"
        for name, info in ALLOWED_ACTIONS.items()
    )
